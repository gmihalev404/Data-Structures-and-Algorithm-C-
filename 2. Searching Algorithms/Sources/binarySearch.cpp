int binarySearch(const vector<int>& arr, int l, int r, int x) {
   
    while (r >= l) { // ако разминем границите - приключваме търсенето
        int m = l + (r - l) / 2; // намираме средния индекс в интервала (l,r)

        if (arr[m] == x) // намерили сме елемента
            return m;

        if (arr[m] < x) // търсим в дясната половина
            l = m + 1;

        else
            r = m - 1; // търсим в лявата половина
    }

    return -1; // елементът не се съдържа в масива
}
